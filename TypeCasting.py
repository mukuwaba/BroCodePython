#type casting = convert the data type of a value to another data type

#variable declarations
x = 1 #int
y = 2.0 #float
z = "3" #string

#Tip: cannot perform math on strings

#Type casting --> convert to strings
x = str(x)
y = str(y)
z = str(z)

print(x)
print(y)
print(z)

#Type casting --> convert to floats
x = float(x)
y = float(y)
z = float(z)

print(x)
print(y)
print(z)

#Type casting --> convert to integers
x = int(x)
y = int(y)
z = int(z)

print(x)
print(y)
print(z)
